    private void RunBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RunBtnActionPerformed
        String Mess;
        if( ( Mess = this.Engine.ExecuteProcedure( this.getProcedure() ) ).equals(org.edu.gces.s2005.projects.frontendformysql.domain.BackEnd.QueryExecuter.EXECUTE_SUCCESS ) ){
            new org.edu.gces.s2005.projects.frontendformysql.ui.Components.ResultSetViewer( null , this.Engine.getResultSet() );
        }
        else{
            javax.swing.JOptionPane.showMessageDialog( this, org.edu.gces.s2005.projects.frontendformysql.ui.Components.DisplayMessageParser.returnParsedMessage( Mess ), " Procedure Execution Failure!!", javax.swing.JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_RunBtnActionPerformed

    private void CompileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CompileBtnActionPerformed
        String Mess = null;
        if( ( Mess = Engine.compileProcedure( this.getProcedure() ) )  == org.edu.gces.s2005.projects.frontendformysql.domain.BackEnd.QueryExecuter.EXECUTE_SUCCESS ){
            onCompilation();
            javax.swing.JOptionPane.showMessageDialog( this, "Procedure was successfully compiled","Procedure Compiled Successfully", javax.swing.JOptionPane.INFORMATION_MESSAGE );
        }
        else{
            javax.swing.JOptionPane.showMessageDialog( this, org.edu.gces.s2005.projects.frontendformysql.ui.Components.DisplayMessageParser.returnParsedMessage( Mess ),"Procedure Compiled Failure", javax.swing.JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_CompileBtnActionPerformed


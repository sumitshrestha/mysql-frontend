    private void addParameters(final org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.ProcedureInterface Proc) {
        
        for( int i= 0; i< Proc.getParameters().length; i++ ){        
        String[] addarray = new String[3];
        
        addarray[0] = Proc.getParameters()[i].getAttName();
        addarray[1] = Proc.getParameters()[i].getAttType();
        addarray[2] = String.valueOf( Proc.getParameters()[i].getAttSize() ) ;
        
        this.ParamTableModel.addRow( addarray );
        }
    }
    

    public org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TableAttributeInterface[] getParameters(){
        if( ParamTable.getRowCount() < 1)
            return ( new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TableAttributeInterface[0] );
        
        // else
        int NameColl = 0;
        int TypeColl = 1;
        int SizeColl = 2;
        
        org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[] array = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute [ ParamTable.getRowCount() ];
        
        for( int i=0 ;i< ParamTable.getRowCount(); i++ ){            
            try{
            array[i] = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute();
            array[i].setName( ParamTable.getValueAt( i, NameColl ).toString() );
            array[i].setType( ParamTable.getValueAt( i, TypeColl ).toString() );
            array[i].setSize( Integer.parseInt( ParamTable.getValueAt( i, SizeColl ).toString() ) );
            }
            catch( Exception e){
                System.out.println( e );
            }
            }        
        return array;
    }
   

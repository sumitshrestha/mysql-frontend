        public org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Table returnDatabaseAffectingTable()
        {
            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Table Tb = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Table();
           
            Tb.setName( getTableName() );
            
            Tb.setType( this.getTableType() );
            
            Tb.setComment( this.getCommentText() );
              
            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[] Att = this.returnAttributes();
            
            for( int i=0; i< Att.length; i++ )
            {
                Tb.addAttribute( Att[i] );
            }
                        
            return Tb;
        }// func ends
        
        private org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[] returnAttributes(){
            
            String[] CollNm = this.returnCollumnNames();
            
            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[] Att = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[ CollNm.length ];
                           
            for( int i=0; i< Att.length; i++ )
            {
             
                Att[i] = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute( );    
        
                Att[i].setName( CollNm[i] ); 
                             
                Att[i].setType( this.returnCollType( CollNm[i] ));
                
                Att[i].setKey( this.getAttributeKey( CollNm[i]) );
                
                try{
                Att[i].setSize( Integer.parseInt( this.getAttributeLength ( CollNm[i] ) ) );
                }
                catch( Exception e )
                {
                    System.out.println( e );
                  
                }
                
            }
            
            
            return Att;
        }
        

    private void addCollumnsBnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCollumnsBnActionPerformed
// TODO add your handling code here:
    Object[] array  = new Object[ 12 ];
    array[0] =  CollNameText.getText();
    array[1] =  ( String ) AttTypeCombo.getSelectedItem();
    array[2] = Boolean.toString( PrimaryKeyCheck.isSelected() ) ;
    array[3] = Boolean.toString( CompulsoryCheck.isSelected() ) ;
    array[4] = Boolean.toString( IndexCheck.isSelected() ) ;
    array[5] = CollLengthText.getText();
    array[6] = CollDefaultText.getText();
    array[9] = CollExtraValText.getText();
    array[10] = Boolean.toString( Signedcheck.isSelected() ) ;
    array[11] = Boolean.toString( zerofillcheck.isSelected() ) ;
    CollTableModel.insertRow(CollumnsTable.getSelectedRowCount() , array ); 
    
    // reset original values 
    this.resetKeyValues();
    
    // give selected focus
    CollNameText.requestFocus();
    }//GEN-LAST:event_addCollumnsBnActionPerformed
    
    

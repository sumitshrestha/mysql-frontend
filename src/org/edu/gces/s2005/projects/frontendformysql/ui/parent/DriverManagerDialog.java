    public void onDriverAddition( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Driver addedDriver ){
        this.DriverListModel.addElement( addedDriver.getDriverName() );       
        org.edu.gces.s2005.projects.frontendformysql.ui.Components.DriverInfoPanel NewDriver = new org.edu.gces.s2005.projects.frontendformysql.ui.Components.DriverInfoPanel( this.Engine );
        NewDriver.setDriverInfo(addedDriver);
        this.CentralPanel.add( addedDriver.getDriverName(),NewDriver );
        this.sp.resetToPreferredSizes();
        this.EditCheck.setEnabled( true );
    }    
    
    public void onDriverServerNameRename( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Driver renamedDriver ){
        org.edu.gces.s2005.projects.frontendformysql.ui.Components.DriverInfoPanel temp = this.getDriverPanel( renamedDriver.getDriverID() );
        temp.setDriverServerName( renamedDriver.getDatabaseServerName());
    }
    
    public void onDriverServerURLRename( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Driver renamedDriver ){
        org.edu.gces.s2005.projects.frontendformysql.ui.Components.DriverInfoPanel temp = this.getDriverPanel( renamedDriver.getDriverID() );
        temp.setDriverServerURL( renamedDriver.getDatabaseServerURL());
    }
    
    public void onDriverDelete( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Driver DeletedDriver ){
        for( int i=0;i< this.DriverList.getModel().getSize(); i++ )
            if( this.DriverList.getModel().getElementAt(i).equals( DeletedDriver.getDriverName() )){                
                this.DriverListModel.remove(i);
                this.CentralPanel.remove(i);
            }
    }
    

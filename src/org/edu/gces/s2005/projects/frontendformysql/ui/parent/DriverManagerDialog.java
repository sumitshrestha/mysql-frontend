    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = this.DriverList.getSelectedIndex();
        
        if( index == -1 ){
            javax.swing.JOptionPane.showMessageDialog( this, "Please select the driver to delete from list.", "Driver Not selected", javax.swing.JOptionPane.ERROR_MESSAGE );
            return;
        }
        
        int result = javax.swing.JOptionPane.showConfirmDialog( this, "Are You sure you wanna delete this Driver?", "Driver Removal Confirmation", javax.swing.JOptionPane.YES_NO_OPTION );
        
        if( result != javax.swing.JOptionPane.OK_OPTION ){
            return;
        }
        
        final String mess = this.Engine.removeDriver( String.valueOf( index ) );
        
        if( mess != org.edu.gces.s2005.projects.frontendformysql.domain.BackEndComponent.DriverModule.DriverManagerInterface.SuccessfullDeletion )
            javax.swing.JOptionPane.showMessageDialog( this, mess, "Driver Deletion Error", javax.swing.JOptionPane.ERROR_MESSAGE );
    }//GEN-LAST:event_jButton2ActionPerformed


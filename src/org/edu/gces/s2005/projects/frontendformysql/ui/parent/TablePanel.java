    public void setValues( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Table table )
    {
       Title = table.getName();
       org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableAttribute[] Atts = table.getAttributes();
        
       try{           
       // fill atts
       for( int i = 0; i<Atts.length; i++)
       {
           Object[] NewRow = new String[6];
           NewRow[0] = Atts[i].getAttName();
           NewRow[1] = Atts[i].getAttType();
           NewRow[2] = Atts[i].getAttKey();
           NewRow[3] = String.valueOf ( Atts[i].getAttSize() );
           NewRow[4] = String.valueOf ( true );
           NewRow[5] = "";
          
           TableAttModel.addRow( NewRow );       
           TableModel.addColumn( Atts[i].getAttName() );       
       }// for ends
       
       for( int i=0;i< table.getRowCount(); i++)
       {
          String [] row = table.returnRow( i );         
         // this.isRefresh = true;
          TableModel.addRow( row );
       }
      
       this.setForeignKeys( table.getForeignKeys() );
       
       try{
           
           if( table.getTriggers() != null ){
       for( int i=0 ;i< table.getTriggers().length; i++ )
       this.addTrigger( table.getTriggers()[i] );
           }// if ends
       }
       catch( Exception e ){
           System.out.println( "error while adding trigger " + e );
       }
       this.addTableUpdateListener();
      }
       catch( Exception e)
       {
       System.out.println( "error in setval"+e );
       }
        
       
    }// func ends
    
    private void setForeignKeys( org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.ForeignKeyInterface [] ForKey ){
        
        if( ForKey.length < 1 )
            return;
        
        final int FOREIGN_KEY_COLLS = 8; // see here IMPORTANT
        for( int i=0 ; i< ForKey.length; i++ ){
           Object[] newrow = new Object[ FOREIGN_KEY_COLLS ];
           newrow[ 0 ] = ForKey[i].getAttName();
           newrow[1] = ForKey[i].getPk_Database();
           newrow[2] = ForKey[i].getPk_Table();
           newrow[3] = ForKey[i].getPK_Name();
           newrow[4 ] = ForKey[i].getDelete_Rule();
           newrow[5 ] = ForKey[i].getUpdate_Rule();
           newrow[6 ] = ForKey[i].getDeferability_Rule();
           newrow[7 ] = "";
           this.TableForeignKeyModel.addRow( newrow );
        }        
    }// func ends
    
    private boolean refresh()
   {
        //1. select its database
            this.Engine.selectDatabase( this.DatabaseNm );
            
       // 1. get domain object of table
        org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.Table Table = Engine.getDatabaseReader().readTable( this.getTableName() , this.DatabaseNm );
               
        // 2. set values
         if( Table != null )
                 {
                this.setValues( Table );                         
                return true;
                }
                 else
                 {
                  // do nothing                     
                  System.out.println( "Table doesnt exist is null" );
                  return false; 
                 }
        
   }// func ends
    

    private void TrigDropBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TrigDropBtnActionPerformed
        int index = this.TrigTable.getSelectedRow();
        
        if( index > -1 ){
            // create a anonymous class for delete event
            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TriggerChangeInterface TriggDropEvt = 
                    new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TriggerChangeInterface(){
                        public org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TableTriggerInterface getChangedTrigger(){
                            
                            final int TRIGG_INDEX = TrigTable.getSelectedRow();
                            final int TRIGG_NAME = 0;
                            final int TRIGG_STATEMENT = 1;
                            final int TRIGG_EVENT = 2;
                            final int TRIGG_TIMING = 3;
                            
                            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableTrigger Trigg 
                                    = new org.edu.gces.s2005.projects.frontendformysql.domain.BackEndData.TableTrigger( TrigTable.getValueAt( TRIGG_INDEX, TRIGG_NAME ).toString() );
                            Trigg.setOperation( TrigTable.getValueAt( TRIGG_INDEX, TRIGG_STATEMENT ).toString() );
                            Trigg.setEvent( TrigTable.getValueAt( TRIGG_INDEX, TRIGG_EVENT ).toString() );
                            Trigg.setTiming( TrigTable.getValueAt( TRIGG_INDEX, TRIGG_TIMING ).toString() );
                            
                            return Trigg;
                        }
    
                        public org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.TableTriggerInterface getNewTrigger(){
                            return null;// no need
                        }
    
                        public String getChangedTableName(){
                            return Title;
                        }
    
                        public String getTriggerDatabase(){
                            return DatabaseNm;
                        }
            };
            
            String Mess = this.Engine.dropTrigger( TriggDropEvt );
            if( Mess == org.edu.gces.s2005.projects.frontendformysql.domain.BackEnd.QueryExecuter.EXECUTE_SUCCESS ){
                this.reinitialize();
                javax.swing.JOptionPane.showMessageDialog( this, "Trigger successfully deleted " , "Trigger drop Success" , javax.swing.JOptionPane.INFORMATION_MESSAGE );                
            }
            else{
                javax.swing.JOptionPane.showMessageDialog( this, org.edu.gces.s2005.projects.frontendformysql.ui.Components.DisplayMessageParser.returnParsedMessage( Mess ), "Trigger Drop Failed" , javax.swing.JOptionPane.ERROR_MESSAGE );
            }
            
        }
        else
        {
            // check if table has any triggers in it i.e. any row is present
            if( this.TrigTable.getRowCount() > 0 )
            javax.swing.JOptionPane.showMessageDialog( this, "Please select the row to be deleted " , "Table Row Not Selected" , javax.swing.JOptionPane.ERROR_MESSAGE );
        }
    }//GEN-LAST:event_TrigDropBtnActionPerformed


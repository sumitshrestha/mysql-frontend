    private void IsolationComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_IsolationComboItemStateChanged
        if( ! this.TransacMan.setCurrentIsolationType( this.IsolationCombo.getSelectedItem().toString() ) )
            javax.swing.JOptionPane.showMessageDialog( null, "Isolation type not set" );
    }//GEN-LAST:event_IsolationComboItemStateChanged

    private void AutoCommCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AutoCommCheckActionPerformed
        if( this.AutoCommCheck.isSelected() )
            this.TransacMan.setAutoCommit();
        else
            this.TransacMan.removeAutoCommit();
    }//GEN-LAST:event_AutoCommCheckActionPerformed


    /**
     * method to initialize other things. must be called after initializing transc man.
     */
    private void initialize(){
        
        if( this.TransacMan == null ){
            return ;
        }
        
        // else
        this.SuppSavePt.setSelected( this.TransacMan.supportsSavePoints() );
        
        this.SuppQueryTpCombo.setSelectedIndex( this.TransacMan.supportsDDLOnly()? 0: 1 );
        
        this.AutoCommCheck.setSelected( this.TransacMan.isAutoCommit() );
        
        this.IsolationCombo.setSelectedItem( this.TransacMan.getCurrentIsolationType() );
    }
    

    final void addNewDatabase( String Db ) 
    {
        try{
        
            javax.swing.tree.DefaultMutableTreeNode DBNode = new javax.swing.tree.DefaultMutableTreeNode( Db );       
            
            DBNode.add( new javax.swing.tree.DefaultMutableTreeNode( this.TABLE ) );
            DBNode.add( new javax.swing.tree.DefaultMutableTreeNode( this.VIEW ) );            
            
            this.fillDatabase( DBNode);
            this.addProcedure( DBNode );
            
            this.DBTreeModel.insertNodeInto( DBNode, this.RootNode, 0 );
        }
        catch( Exception e)
        {
            System.out.println( e +" in addnewdb" );
        }
    }// func ends
    
    
    
    private final void addProcedure( javax.swing.tree.DefaultMutableTreeNode DBNode ){
        String[]  list = this.Engine.getDatabaseReader().getProcedureList( DBNode.toString() );
        javax.swing.tree.DefaultMutableTreeNode ProcNode = new javax.swing.tree.DefaultMutableTreeNode( this.PROCEDURE );
        
        if( list != null ){
        for( int i=0 ; i< list.length; i++){
            javax.swing.tree.DefaultMutableTreeNode addproc = new javax.swing.tree.DefaultMutableTreeNode( list[i] );
            ProcNode.add( addproc );
        }
        }
        
        DBNode.add( ProcNode );
    }// func ends
    
    

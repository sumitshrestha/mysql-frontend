   public final void fillDatabase( javax.swing.tree.DefaultMutableTreeNode DbNd){
       try{
      
       String[] array = this.Engine.getDatabaseReader().returnTableList( DbNd.toString() );
       
       if( array == null )
           return;
       
       for( int i=0; i< array.length; i++){         
                 for( int j=0; j<DbNd.getChildCount(); j++){                 
                        this.DBTreeModel.insertNodeInto( (javax.swing.tree.MutableTreeNode) new javax.swing.tree.DefaultMutableTreeNode( array[i] ) ,(javax.swing.tree.MutableTreeNode)DbNd.getChildAt(j) , 0 );                 
                 }
       }       
       }
       catch( Exception e)
       {
           System.out.println( e + " exception in filling table node 4 database " + DbNd.toString() );
       }
   }
    
   

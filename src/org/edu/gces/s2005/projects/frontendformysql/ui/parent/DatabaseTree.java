    private final void disablePopupMenus() {
        if ( this.DatabaseMenu.isVisible() )
        {
            this.DatabaseMenu.setVisible(false );
        }
        else
            if ( this.DatabaseNodeMenu.isVisible() )
        {
            this.DatabaseNodeMenu.setVisible( false );
        }
            else
                if( this.TableMenu.isVisible() ){
                    this.TableMenu.setVisible( true );
                }
                else
                    if( this.ViewNodeMenu.isVisible() )
                        this.ViewNodeMenu.setVisible( false );
                    else
                        if( this.ViewMenu.isVisible() )
                            this.ViewMenu.setVisible( false );
                        else
                            if( this.TableNodeMenu.isVisible() )
                                this.TableNodeMenu.setVisible( false );
    }

    

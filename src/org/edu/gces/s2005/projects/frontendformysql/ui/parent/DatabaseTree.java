   public javax.swing.tree.DefaultMutableTreeNode returnChild( javax.swing.tree.DefaultMutableTreeNode Parent , String Child )
   {
       for( int i=0;i<Parent.getChildCount(); i++)
       {
           javax.swing.tree.DefaultMutableTreeNode node = ( javax.swing.tree.DefaultMutableTreeNode ) Parent.getChildAt( i );
           if( node.toString().equals( Child ) )
           {
               return node;
           }
       }
       // else if Child dosent exists
       return null;
   }
  
   void addTableToDatabaseTree(final String TbName, final String Db  ) {
        javax.swing.tree.DefaultMutableTreeNode NewTable = new javax.swing.tree.DefaultMutableTreeNode( TbName );
        javax.swing.tree.DefaultMutableTreeNode db = this.returnChild( RootNode, Db );
        if( db != null ) {
            //( (javax.swing.tree.DefaultMutableTreeNode)db.getChildAt(0) ).add( NewTable );
            DBTreeModel .insertNodeInto( NewTable , (javax.swing.tree.DefaultMutableTreeNode) db.getChildAt(0) , db.getChildAt(0).getChildCount());
            
            //Make sure the user can see the lovely new node.
            
            DBTree.scrollPathToVisible(new javax.swing.tree.TreePath( NewTable.getPath()) );
            
        } else {
            // report error that databases doesnt exists
            System.out.println("databases doesnt exists");
        }
    }
   

    private void DBTreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DBTreeMouseClicked
        this.disablePopupMenus();
        try{                   
        javax.swing.tree.TreePath SelectedPath = this.DBTree.getClosestPathForLocation( evt.getX(), evt.getY());
        if( SelectedPath == null ){
            return;
        }
        if( this.DBTree.getPathBounds(SelectedPath).contains( evt.getX(), evt.getY()) ){                
            javax.swing.tree.TreeNode ClickedNode = (javax.swing.tree.TreeNode) SelectedPath.getLastPathComponent();
            this.DBTree.setSelectionPath(SelectedPath);
            System.out.println( "This is the selected node " + ClickedNode.toString() );
        }
        else
            return;
        
        if( this.isMenuClick( evt )){
                
                if( this.getSelectednode() == this.RootNode  ) 
                    this.displayPopupMenu( evt, DatabaseNodeMenu );
                
                else
                    if( this.isSelectedNodeDatabase() )                        
                        displayPopupMenu(evt, this.DatabaseMenu );
                    
                    else
                        if (  this.getSelectednode().getParent().toString() == this.TABLE )
                            this.displayPopupMenu( evt, TableMenu );
                        
                        else
                            if( this.getSelectednode().toString() == this.VIEW )
                                this.displayPopupMenu( evt, ViewNodeMenu );
                            
                            else
                                if( this.getSelectednode().getParent().toString() == this.VIEW )
                                    this.displayPopupMenu( evt, ViewMenu );
                                
                                else
                                    if( this.getSelectednode().toString() == this.TABLE )
                                        this.displayPopupMenu( evt, TableNodeMenu );
            } 
            else
                if( this.isSingleClick( evt )){
                    if (  this.getSelectednode().getParent().toString() == this.TABLE ){
                        this.TableMenu.setVisible( false );
                        this.ParentFrame.displayTable( this.getSelectednode() );
                    }
                    else
                        if( this.getSelectednode().getParent().toString() == this.VIEW ){
                            this.ParentFrame.displayTable( this.getSelectednode() );
                        }                    
                                    else
                                        if( this.getSelectednode().getParent().toString() == this.PROCEDURE ){ // for any procedure node click                                           
                                            org.edu.gces.s2005.projects.frontendformysql.domain.BackEndInterfaces.ProcedureInterface Proc = null;
                                            Proc = this.Engine.getDatabaseReader().readProcedure( this.getSelectednode().toString(), this.Engine.returnSelectedDatabase() );
                                            if( Proc != null ){
                                                org.edu.gces.s2005.projects.frontendformysql.ui.parent.ProcedureBuilderPanel p = new org.edu.gces.s2005.projects.frontendformysql.ui.parent.ProcedureBuilderPanel( Proc, this.Engine, this.Engine.returnSelectedDatabase() );
                                                this.ParentFrame.displayComponet( p, "Procedure::"+ Proc.getName()  );
                                            } 
                                        }
            
                }                 
        } catch( Exception e) {
        }
    }//GEN-LAST:event_DBTreeMouseClicked


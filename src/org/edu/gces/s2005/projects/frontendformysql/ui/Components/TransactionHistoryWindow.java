    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        org.edu.gces.s2005.projects.frontendformysql.ui.Forms.SavePointInputForm form = new org.edu.gces.s2005.projects.frontendformysql.ui.Forms.SavePointInputForm( null, true );
        form.setVisible( true );
        
        if( form.getState() == form.OK ){
            String Nm = form.getSavePoint();            
            final String Mess = this.TransacMan.createSavePoint( Nm );
            
            if( Mess != this.TransacMan.SAVEPOINT_CREATED ){
                javax.swing.JOptionPane.showMessageDialog( this, Mess, "Save Point Not Created", javax.swing.JOptionPane.ERROR_MESSAGE );
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    

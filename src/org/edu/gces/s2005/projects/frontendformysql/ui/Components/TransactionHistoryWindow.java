    private void RollBackBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RollBackBtnActionPerformed
        int row = this.HistoryTable.getSelectedRow();
        
        if( row < 0 )
            return;
        
        //else
        Object val = this.HistoryTable.getValueAt( row, 2 );
        
        if( val == this.NO_SavePoint ){
            javax.swing.JOptionPane.showMessageDialog( this, "No Save Point Here so Returning","No Save Point Error!!", javax.swing.JOptionPane.ERROR_MESSAGE );
            return; // there was no save point created here so no realease
        }
        
        // else
        this.TransacMan.rollback( val.toString() );
        
    }//GEN-LAST:event_RollBackBtnActionPerformed


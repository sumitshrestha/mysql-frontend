    private void CentralTabStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_CentralTabStateChanged
        // this try block is for settings of the table        
        try{
            org.edu.gces.s2005.projects.frontendformysql.ui.parent.TablePanel p = ( org.edu.gces.s2005.projects.frontendformysql.ui.parent.TablePanel ) this.CentralTab.getSelectedComponent();
                try{                    
                    this.Engine.selectDatabase( p.getDatabaseName() );
                    this.Enabler.whenNotEditing();
                    this.StBar.setStatus( "woring on table..." );
                }
                catch( Exception e ){
                    // report
                    this.StBar.setStatus( "cannot set database..." );
                }
            }
        catch( java.lang.ClassCastException e ){
            boolean state = this.setEditActions();
            
            if( state ){// check if editing is being done                
                // enable file buttons 
                // enabling edit is not need since it is automatically done when adding action 
                this.Enabler.whenEditing();
                this.StBar.setStatus( "editing being done..." );
            }
            else{
                // means it is neither editing nor table like terminal or any future components that will be added.
                // so disable editing things
                this.Enabler.whenNotEditing();                
            }
        }
    }//GEN-LAST:event_CentralTabStateChanged


    final void displayComponet( java.awt.Component panel, String TabTitle )
    {            
                 if( ! this.isAlreadyOpenedinTab( TabTitle ) ) { // check if table has already been opened
                 CentralTab.add( TabTitle , panel );  // adding the panel to the central tab with title viz name of database/name of table               
                 org.edu.gces.s2005.projects.frontendformysql.ui.Components.ButtonTabComponent.initTabComponent( CentralTab , CentralTab.getTabCount() -1 );
                 CentralTab.setSelectedIndex( CentralTab.getTabCount() -1 );                   
                 }
                 else
                 {
                  // do nothing                     
                 }
    }
        
    

   public final boolean removeTab( String TabTitle )
   {
       for( int i=0; i< CentralTab.getTabCount(); i++ )
           if( CentralTab.getTitleAt( i ).equals( TabTitle ) ){
           CentralTab.remove( i );
           return true;
           }        
       
       return false;
   }
    
